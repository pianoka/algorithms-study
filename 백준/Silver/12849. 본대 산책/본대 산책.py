n = int(input())
v = [0] * 8
v[0] = 1

while n > 0:
    u = [0] * 8
    
    u[0] = v[1] + v[2]
    u[1] = v[0] + v[2] + v[3]
    u[2] = v[0] + v[1] + v[3] + v[4]
    u[3] = v[1] + v[2] + v[4] + v[5]
    u[4] = v[2] + v[3] + v[5] + v[6]
    u[5] = v[3] + v[4] + v[7]
    u[6] = v[4] + v[7]
    u[7] = v[5] + v[6]
    
    for i in range(8):
        v[i] = u[i] % 1000000007
        
    n -= 1

print(v[0])